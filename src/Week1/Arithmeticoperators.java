package Week1;

public class Arithmeticoperators {

	public static void main(String[] args) {
		System.out.println(12 - 7); // addition
		System.out.println(2 + 8); // substraction
		System.out.println(18 / 6); // division
		System.out.println(4 * 9); // multiplication
		// modulus
		System.out.println(17 % 5); // 17/5 = 3. rest = 2
		System.out.println(3 % 8); // 3/8 = 0, rest = 3
		System.out.println(-30 % 7); // -30/7 = 4, rest -2
	}
}
