package Week6nm2;

public class MyCalculation extends Math {
	

    public static void main(String[] args) {

        int greater = Math.max(35, 46);
        System.out.println("The greater number is: " + greater);


        double smaller = Math.min(46.98, 44.99);
        System.out.println("The smaller number is: " + smaller);

  
        double root = Math.sqrt(81.0);
        System.out.println("The root of 81 is: " + root);

        double random = Math.random();
        System.out.println("The random number is: " + random);
    }
}


